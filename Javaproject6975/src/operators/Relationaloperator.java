package operators;

public class Relationaloperator {

	public static void main(String[] args) {
		int a = 10;
		int b = 20;

		
		System.out.println("---------------Relationaloperator--------------");
		
		System.out.println("print equal to   =" + (a == b));
		System.out.println("print not equal to    =" + (a != b));
		System.out.println("print greater than    =" + (a > b));
		System.out.println("print less than   =" + (a < b));
		System.out.println("print greater than equal to  =" + (a <= b));
		System.out.println("print less than equal to   =" + (a >= b));

	}

}
